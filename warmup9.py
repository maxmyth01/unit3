#warmup9.py - asks for a word and prints out letters in capitals

word = input('Enter a word: ')

for ch in word:
    if ch == 'a':
        print(ch.upper())
    elif ch == 'e':
        print(ch.upper())
    elif ch == 'i':
        print(ch.upper())
    elif ch == 'o':
        print(ch.upper())
    elif ch == 'u':
        print(ch.upper())
    else:
        print(ch)


    
